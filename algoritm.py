#Функция `is_palindrome`** принимает строку `s` в качестве аргумента.
#. **С помощью `split()` и `join()`** удаляются все пробелы, а метод `lower()` приводит строку к нижнему регистру.
#. **Сравниваем очищенную строку** с её обратной версией, используя срез `[::-1]`.
# Если строки совпадают, функция возвращает `True`, в противном случае — `False`.



def is_palindrome(s):
    """Проверяет, является ли строка палиндромом."""

    # 1. Удаляем пробелы и приводим строку к нижнему регистру
    cleaned_str = ''.join(s.split()).lower()

    # 2. Сравниваем строку с её обратной версией
    return cleaned_str == cleaned_str[::-1]


# Примеры использования функции:
print(is_palindrome("А роза упала на лапу Азора"))  # True
print(is_palindrome("Привет"))  # False
print(is_palindrome("Madam I'm Adam"))  # False
print(is_palindrome("Не на, не на!"))  # True